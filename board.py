from pot import Pot


board_format = """______________________________________
| [{13:>2}] ({12:>2})({11:>2})({10:>2})({9:>2})({8:>2})({7:>2}) [  ] |
| [  ] ({0:>2})({1:>2})({2:>2})({3:>2})({4:>2})({5:>2}) [{6:>2}] |
______________________________________
"""

class Board:
    """Mancala board.
    
    Board layout with pot indexs'.
    ______________________________________
    | [13] (12)(11)(10)( 9)( 8)( 7) [  ] |
    | [  ] ( 0)( 1)( 2)( 3)( 4)( 5) [ 6] |
    ______________________________________

    """
    pots = [Pot(i) for i in range(14)]
    
    @property
    def state(self):
        return [pot.count for pot in self.pots]
    